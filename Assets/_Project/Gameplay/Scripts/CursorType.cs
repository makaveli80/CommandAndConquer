namespace CommandAndConquer.Gameplay
{
    /// <summary>
    /// Types de curseurs disponibles pour le gameplay.
    /// </summary>
    public enum CursorType
    {
        /// <summary>
        /// Curseur par défaut du système.
        /// </summary>
        Default,

        /// <summary>
        /// Curseur lors du survol d'une unité amie (animé).
        /// </summary>
        Hover,

        /// <summary>
        /// Curseur pour indiquer une destination de mouvement.
        /// </summary>
        Move
    }
}